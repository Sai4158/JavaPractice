package mvc_prac2;

public class StudentModel {
	private String name;
	private int rollNo;
	
	
//	get and setters

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getRollNo() {
		return rollNo;
	}
	public void setRollNo(int rollNo) {
		this.rollNo = rollNo;
	}
		
}
