package mvc_GUI;
//MODEL
public class StudentModel {
	
//	variables
	private String name;
	private int rollno;
	private boolean car;
	
	
//	------------
//	get and set start
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getRollno() {
		return rollno;
	}
	public void setRollno(int rollno) {
		this.rollno = rollno;
	}
	public boolean isCar() {
		return car;
	}
	public void setCar(boolean car) {
		this.car = car;
	}
	
//	get and set end
//	------------
	
}
