package a_DSA_part_41_to_part_50;

public class Day42ExtractDigits {
	public static void main(String[] args) {

		int a = 25;

		int b =  a % 10;

		System.out.println(b); //5



		int a1 = 235;

		int b1 =  a1 % 10;

		System.out.println(b1); //5



		int a2 = 235;

		int b2 =  a2 / 10;

		System.out.println(b2); //23
	}
}
