package a_DSA_part_11_to_part_20;

public class Day14_While {
    public static void main(String[] args) {
        
        int i = 0;
        
        while(i <= 10) {
            System.out.println(i);
            
            // Increment `i` in each iteration
            if(i == 9) {
                System.out.println("Done");
            }
            
            i++;
        }
        
        
        
        
    }
}
